void main(){
  var x = 1;
  ///postfix  prefix
  ///postfix incre / decre eg: x++ => x = x+1
  ///prefix incre / decre eg: x-- => x = x-1


  print(++x); //x=1 x = x+1 = 2
  print(--x); //x = x-1 = 1

  print("print pattern");
  
  print(x);
  print(++x);
  print(++x);
  print(++x);
  print(++x);
  print(x);
  print(--x);
  print(--x);
  print(--x);
  print(--x);
}